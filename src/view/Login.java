    private void txtPasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtPasswordActionPerformed
        if (ascii == 10) {
            try {
                Statement stmt = koneksi.createStatement();
                String query = "select * from t_login where username = '" + txtUsername.getText() + "'AND password = '" + txtPassword.getText() + "'";
                stmt = koneksi.createStatement();
                ResultSet rs = stmt.executeQuery(query);
                if (rs.next()) {

                    JOptionPane.showMessageDialog(this, "Welcome To : " + txtUsername.getText() + "", "Informasi", JOptionPane.INFORMATION_MESSAGE);

                    new Login().setVisible(true);

                    dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Identitas : " + txtUsername.getText() + " : Tidak diketahui!!!", "Informasi", JOptionPane.ERROR_MESSAGE);
                    txtUsername.setText("");
                    txtPassword.setText("");
                    txtUsername.requestFocus();

                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "gagal login" + e.getMessage());

            }
        }
    }//GEN-LAST:event_txtPasswordActionPerformed

