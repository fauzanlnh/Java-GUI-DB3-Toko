    private void bLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bLoginActionPerformed
        // TODO add your handling code here:
        try {
            Statement stmt = koneksi.createStatement();
            String query = "SELECT * FROM t_login WHERE username='" + txtUsername.getText() + "' AND password='" + txtPassword.getText() + "'";
            stmt = koneksi.createStatement();
            ResultSet rs = stmt.executeQuery(query);
            if (rs.next()) {
                if (txtUsername.getText().equals(rs.getString("username")) && txtPassword.getText().equals(rs.getString("password"))) {
                    JOptionPane.showMessageDialog(null, "berhasil login");
                }
                new MenuUtama().setVisible(true);

                dispose();
            } else {
                JOptionPane.showMessageDialog(null, "username atau password salah");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }

    }//GEN-LAST:event_bLoginActionPerformed

